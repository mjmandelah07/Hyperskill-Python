# Boolean type
# The Boolean type, or simply bool, is a special data type that has only two possible values: True and False.
# In Python, the names of boolean values start with a capital letter.
is_open = True
is_closed = False

print(is_open)    # True
print(is_closed)  # False

# Boolean operations
# There are three built-in boolean operators in Python: and, or and not.
# 1. and is a binary operator, it takes two arguments and returns True if both arguments are true,
# otherwise, it returns False.
# a = True and True    # True
# b = True and False   # False
# c = False and False  # False
# d = False and True   # False


# 2. or is a binary operator, it returns True if at least one argument is true, otherwise, it returns False.
# a = True or True    # True
# b = True or False   # True
# c = False or False  # False
# d = False or True   # True

# 3. not is a unary operator, it reverses the boolean value of its argument.
# to_be = True           # to_be is True
# not_to_be = not to_be  # not_to_be is False

